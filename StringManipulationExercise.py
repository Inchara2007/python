a = input("Sentence") #input taken from user
print("UPPERCASE: " +a.upper()) #to print in uppercase
print("LOWERCASE: " +a.lower()) #to print in lowercase
print("REPLACE: " +a.replace(" ","_")) #to replace with new value, here it is underscore(_)
print("STRIPPED: "+a.strip()) #to clear leading and trailing(back to original)
